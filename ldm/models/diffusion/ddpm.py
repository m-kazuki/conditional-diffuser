        x_noisy = self.q_sample(x_start=x_start, t=t, noise=noise)
        
        # Condition start and goal
        x_noisy[:,:2,0]=cond[:,:,0].clone()
        x_noisy[:,:2,-1]=cond[:,:,1].clone()

        model_output = self.apply_model(x_noisy, cond, t)

        # Condition start and goal
        model_output[:,:2,0]=cond[:,:,0].clone()
        model_output[:,:2,-1]=cond[:,:,1].clone()

